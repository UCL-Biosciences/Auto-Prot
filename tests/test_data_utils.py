def test_combine_csv_files_outputs_combined(mock_csv_structure):
    """
    Verify that combine_csv_files merges top-N rows from multiple files
    and adds a 'treatment_pair' column to distinguish them.
    """
    combine_csv_files(
        filename="top_hits.csv",
        output_dir=mock_csv_structure,
        top_n=2,
        new_column="treatment_pair"
    )

    # Combined file should be written to data/combined_top_hits.csv
    combined_path = os.path.join(mock_csv_structure, "data", "combined_top_hits.csv")
    assert os.path.exists(combined_path)

    df = pd.read_csv(combined_path)
    assert df.shape[0] == 4  # 2 rows from each file
    assert "treatment_pair" in df.columns
    assert set(df["treatment_pair"]) == {"DrugA", "DrugB"}
