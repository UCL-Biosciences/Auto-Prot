def test_combine_plots_creates_output(image_dir_with_pngs):
    """
    Test that combine_plots finds and tiles images, saving to expected output path.
    """
    output_dir = tempfile.mkdtemp()

    combine_plots(
        search_term="volcano_plot.png",
        search_path=image_dir_with_pngs,
        output_dir=output_dir,
        img_size=(100, 100),
        max_cols=2
    )

    # Final path based on default combine logic
    combined_path = os.path.join(output_dir, "plots", "combined_volcano_plot.png")
    assert os.path.exists(combined_path)

    with Image.open(combined_path) as img:
        assert img.size[0] >= 100  # width
        assert img.size[1] >= 100  # height

    shutil.rmtree(output_dir)


