def test_apply_row_id_config_phospho_fields_present():
    """
    Test that phospho row IDs are generated by combining index and specified fields.
    - Missing fields should be replaced with 'NA' as per config.
    - Final index format: <index>__<field1>_<field2>.
    """
    df = pd.DataFrame({
        "site": ["S10", "T20", None],
        "modification": ["phospho", "phospho", "acetyl"]
    }, index=["P1", "P2", "P3"])

    config = {
        "data_type": "phospho",
        "phospho_row_id": {
            "fields": ["site", "modification"],
            "missing_value": "NA"
        }
    }

    result = apply_row_id_config(df.copy(), config)

    # Expect site and modification to be appended to the original index
    expected_index = [
        "P1__S10_phospho",
        "P2__T20_phospho",
        "P3__NA_acetyl"  # 'site' is None -> replaced by "NA"
    ]
    assert list(result.index) == expected_index


def test_apply_row_id_config_non_phospho():
    """
    Test that non-phospho data_type skips row ID transformation.
    Function should return DataFrame unchanged.
    """
    df = pd.DataFrame({"A": [1, 2]}, index=["X", "Y"])
    config = {"data_type": "other"}
    result = apply_row_id_config(df.copy(), config)
    assert result.equals(df)


def test_apply_row_id_config_missing_field():
    """
    Test that only available fields are used to generate new index.
    Missing fields in config are silently ignored.
    """
    df = pd.DataFrame({
        "site": ["S10", "T20"]
    }, index=["P1", "P2"])

    config = {
        "data_type": "phospho",
        "phospho_row_id": {
            "fields": ["site", "modification"],  # 'modification' is missing
            "missing_value": "NA"
        }
    }

    result = apply_row_id_config(df.copy(), config)

    # Only 'site' is used; no 'modification' column available
    expected_index = ["P1__S10", "P2__T20"]
    assert list(result.index) == expected_index


def test_apply_row_id_config_no_fields():
    """
    If no fields are specified in config, function should return DataFrame unchanged.
    """
    df = pd.DataFrame({"A": [1, 2]}, index=["X", "Y"])
    config = {
        "data_type": "phospho",
        "phospho_row_id": {"fields": []}
    }
    result = apply_row_id_config(df.copy(), config)
    assert result.equals(df)


