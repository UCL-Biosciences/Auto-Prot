def test_get_subset_success():
    """
    Test that get_subset() correctly filters columns based on metadata values.
    - Input data has columns A_rep1, A_rep2, B_rep1.
    - Subset by 'treatment' == 'A' -> returns A_rep1, A_rep2 only.
    """
    df = pd.DataFrame({
        "A_rep1": [1.0, 2.0],
        "A_rep2": [1.5, 2.5],
        "B_rep1": [3.0, 4.0],
    }, index=["Prot1", "Prot2"])

    metadata = pd.DataFrame({
        "sample_rep": ["A_rep1", "A_rep2", "B_rep1"],
        "treatment": ["A", "A", "B"],
    })

    result = get_subset(df, "A", metadata, "treatment")

    assert list(result.columns) == ["A_rep1", "A_rep2"]
    assert result.shape == (2, 2)


def test_get_subset_no_match():
    """
    Test that ValueError is raised when no metadata rows match the subset term.
    """
    df = pd.DataFrame({
        "A_rep1": [1.0, 2.0],
        "B_rep1": [3.0, 4.0],
    }, index=["Prot1", "Prot2"])

    metadata = pd.DataFrame({
        "sample_rep": ["A_rep1", "B_rep1"],
        "treatment": ["A", "B"],
    })

    with pytest.raises(ValueError, match="No matches found for subset: C"):
        get_subset(df, "C", metadata, "treatment")


