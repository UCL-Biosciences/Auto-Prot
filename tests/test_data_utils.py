def good_metadata():
    """
    Return a valid example metadata DataFrame for use in multiple tests.
    """
    return pd.DataFrame({
        "sample_id": ["S1", "S2"],
        "treatment": ["Ctl", "Drug"],
        "replicate": [1, 2],
        "protein_abundance_name": ["col1", "col2"],
        "sample_rep": ["S1_1", "S2_1"]
    })


def test_validate_metadata_passes():
    """
    Basic test: all required columns are present and valid.
    Should pass without raising.
    """
    validate_metadata(good_metadata())


def test_missing_column_raises():
    """
    Test that missing required columns raise a ValueError.
    """
    df = good_metadata().drop(columns="treatment")
    with pytest.raises(ValueError, match="Missing required columns"):
        validate_metadata(df)


def test_nan_raises():
    """
    Test that missing values (NaN) in required columns raise a ValueError.
    """
    df = good_metadata()
    df.loc[1, "sample_id"] = None
    with pytest.raises(ValueError, match="contains missing"):
        validate_metadata(df)


def test_duplicate_sample_replicate_pair_raises():
    """
    Duplicate sample_id + replicate combinations are not allowed.
    """
    df = good_metadata()
    df.loc[1, "sample_id"] = "S1"
    df.loc[1, "replicate"] = 1
    df.loc[1, "sample_rep"] = "S1_1"
    with pytest.raises(ValueError, match="must be unique"):
        validate_metadata(df)


def test_nonnumeric_replicate_raises():
    """
    Replicate must be numeric (int or float), not string.
    """
    df = good_metadata()
    df["replicate"] = ["one", "two"]
    with pytest.raises(ValueError, match="'replicate' must be numeric"):
        validate_metadata(df)


def test_duplicate_protein_abundance_name_raises():
    """
    Duplicated protein_abundance_name values should raise an error.
    """
    df = good_metadata()
    df.loc[1, "protein_abundance_name"] = "col1"
    with pytest.raises(ValueError, match="must not contain duplicate"):
        validate_metadata(df)


